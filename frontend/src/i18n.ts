export type T = typeof translations.en;

export function useT(language: Language | undefined | null): T {
  const lang = (language && translations[language]) ? language : 'en';
  return translations[lang] as T;
}

export function isRTL(language: Language): boolean {
  return language === 'ar' || language === 'ku';
}

// Map default English category names to translation keys so categories
// created on the backend in English still render in the active language.
const DEFAULT_CATEGORY_KEYS: Record<string, keyof T> = {
  'salary': 'cat_salary',
  'freelance': 'cat_freelance',
  'investment': 'cat_investment',
  'other income': 'cat_other_income',
  'food & dining': 'cat_food',
  'food and dining': 'cat_food',
  'food': 'cat_food',
  'transportation': 'cat_transport',
  'transport': 'cat_transport',
  'housing': 'cat_housing',
  'entertainment': 'cat_entertainment',
  'health': 'cat_health',
  'shopping': 'cat_shopping',
  'education': 'cat_education',
  'other': 'cat_other',
};

export function translateCategoryName(name: string | undefined | null, t: T): string {
  if (!name) return '';
  const key = DEFAULT_CATEGORY_KEYS[name.trim().toLowerCase()];
  return key ? (t[key] as string) : name;
}

